package VO;

public class TwishVO {
	private int twpk;
	private int tupk;
	private int trpk;
	public int getTwpk() {
		return twpk;
	}
	public void setTwpk(int twpk) {
		this.twpk = twpk;
	}
	public int getTupk() {
		return tupk;
	}
	public void setTupk(int tupk) {
		this.tupk = tupk;
	}
	public int getTrpk() {
		return trpk;
	}
	public void setTrpk(int trpk) {
		this.trpk = trpk;
	}
	@Override
	public String toString() {
		return "TwishVO [twpk=" + twpk + ", tupk=" + tupk + ", trpk=" + trpk + "]";
	}
	
}
